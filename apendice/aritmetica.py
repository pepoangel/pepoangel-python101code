def sumar(x, y):
    return x + y   # suma los valores de x e y

def dividir(x, y):
    return x / y   # divide el valor de x entre el valor de y

def multiplicar(x, y):
    return x * y   # multiplica los valores de x e y

def restar(x, y):
    return x - y   # resta el valor de y al valor de x
